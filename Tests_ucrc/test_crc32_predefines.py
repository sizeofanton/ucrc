from ucrc import crc32_bzip2
from ucrc import crc32_c
from ucrc import crc32_d
from ucrc import crc32_mpeg2
from ucrc import crc32_posix
from ucrc import crc32_q
from ucrc import crc32_jamcrc
from ucrc import crc32_xfer


def test_crc32_bzip2():
    assert crc32_bzip2(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x531B49A4


def test_crc32_c():
    assert crc32_c(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x8ECB7537


def test_crc32_d():
    assert crc32_d(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x54167DA3


def test_crc32_mpeg2():
    assert crc32_mpeg2(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0xACE4B65B


def test_crc32_posix():
    assert crc32_posix(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x6961F5D6


def test_crc32_q():
    assert crc32_q(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x703D6A62


def test_crc32_jamcrc():
    assert crc32_jamcrc(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x92C663CD


def test_crc32_xfer():
    assert crc32_xfer(bytes([0xFF, 0xFE, 0xFA, 0xF0, 0xFF, 0x00])) == 0x2906200B
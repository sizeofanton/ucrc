from ucrc import crc64_ecma_182
from ucrc import crc64_go_iso
from ucrc import crc64_we
from ucrc import crc64_xz


def test_crc64_ecma_182():
    assert crc64_ecma_182(bytes([0x31,0x32,0x33,0x34,0x35,0x36,0x37,0x38,0x39])) == 0x6C40DF5F0B497347


def test_crc64_go_iso():
    assert crc64_go_iso(bytes([0x31,0x32,0x33,0x34,0x35,0x36,0x37,0x38,0x39])) == 0xB90956C775A41001


def test_crc64_we():
    assert crc64_we(bytes([0x31,0x32,0x33,0x34,0x35,0x36,0x37,0x38,0x39])) == 0x62EC59E3F1A4F00A


def test_crc64_xz():
    assert crc64_xz(bytes([0x31,0x32,0x33,0x34,0x35,0x36,0x37,0x38,0x39])) == 0x995DC9BBDF1939FA